
function suma(a, b) {
    return a + b;
  }
  
  function resta(a, b) {
    return a - b;
  }

  function multiplicacion(a, b) {
    return a * b;
  }

  function division(a, b) {
    return a / b;
  }

  function raizCuadrada(a) {
    return Math.sqrt(a);
  }
  module.exports = { suma, resta, multiplicacion, division,raizCuadrada };
